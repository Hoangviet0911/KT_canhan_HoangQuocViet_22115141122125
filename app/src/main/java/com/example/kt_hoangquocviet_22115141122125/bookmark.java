// Bookmark.java
public class bookmark {
    private String title;
    private String description;

    public bookmark(String title, String description) {
        this.title = title;
        this.description = description;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }
}

